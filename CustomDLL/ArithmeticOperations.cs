namespace CustomDLL
{
    public  static class ArithmeticOperations
    {
        public static int Sum(int a, int b)
        {
            return a + b;

        }
        public static int Subtract(int a, int b)
        {
            return a - b;
        }
        public static int Multiple(int a, int b)
        {
            return a * b;
        }
        public static int Divide(int a, int b)
        {
            return a / b;
        }

    }
}